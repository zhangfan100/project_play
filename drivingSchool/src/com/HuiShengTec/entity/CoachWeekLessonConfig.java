package com.HuiShengTec.entity;

import java.io.Serializable;

/**
 * @author zf
 *
 */
public class CoachWeekLessonConfig implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private Integer id;
	private Integer coachId;
	private Integer weekDay;
	private Integer timeSlot;
	private Integer subject;
	private Integer timeSlotType;
	private String op;
	
	
	public String getName() {
		return op;
	}

	public void setName(String op) {
		this.op = op;
	}

	public CoachWeekLessonConfig() {
	}
	
	public CoachWeekLessonConfig(Integer id, Integer coachId, Integer weekDay,
			Integer timeSlot, Integer subject, Integer timeSlotType) {
		this.id = id;
		this.coachId = coachId;
		this.weekDay = weekDay;
		this.timeSlot = timeSlot;
		this.subject = subject;
		this.timeSlotType = timeSlotType;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCoachId() {
		return coachId;
	}
	public void setCoachId(Integer coachId) {
		this.coachId = coachId;
	}
	public Integer getWeekDay() {
		return weekDay;
	}
	public void setWeekDay(Integer weekDay) {
		this.weekDay = weekDay;
	}
	public Integer getTimeSlot() {
		return timeSlot;
	}
	public void setTimeSlot(Integer timeSlot) {
		this.timeSlot = timeSlot;
	}
	public Integer getSubject() {
		return subject;
	}
	public void setSubject(Integer subject) {
		this.subject = subject;
	}
	public Integer getTimeSlotType() {
		return timeSlotType;
	}
	public void setTimeSlotType(Integer timeSlotType) {
		this.timeSlotType = timeSlotType;
	}
	  
}
