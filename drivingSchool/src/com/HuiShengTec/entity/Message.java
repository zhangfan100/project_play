package com.HuiShengTec.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * @author llj
 *
 */
public class Message implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private Integer id;
	private Integer kindID;
	private String title;
	private String content;
	private Date messageDate;
	private Integer isDisplay;
	private Integer userId;
	private Integer isTop;
	private Integer isHot;
	private Long readCount;
	private Integer seperateSchoolId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getKindID() {
		return kindID;
	}
	public void setKindID(Integer kindID) {
		this.kindID = kindID;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getMessageDate() {
		return messageDate;
	}
	public void setMessageDate(Date messageDate) {
		this.messageDate = messageDate;
	}
	public Integer getIsDisplay() {
		return isDisplay;
	}
	public void setIsDisplay(Integer isDisplay) {
		this.isDisplay = isDisplay;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getIsTop() {
		return isTop;
	}
	public void setIsTop(Integer isTop) {
		this.isTop = isTop;
	}
	public Integer getIsHot() {
		return isHot;
	}
	public void setIsHot(Integer isHot) {
		this.isHot = isHot;
	}
	
	public Long getReadCount() {
		return readCount;
	}
	public void setReadCount(Long readCount) {
		this.readCount = readCount;
	}
	public Integer getSeperateSchoolId() {
		return seperateSchoolId;
	}
	public void setSeperateSchoolId(Integer seperateSchoolId) {
		this.seperateSchoolId = seperateSchoolId;
	}
	

}
