package com.HuiShengTec.entity;

import java.io.Serializable;

/**
 * @author llj
 *
 */
public class MessageKind implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String kindName;
	private Integer schoolSeptFlag;
	private String kindKey;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getKindName() {
		return kindName;
	}
	public void setKindName(String kindName) {
		this.kindName = kindName;
	}
	public Integer getSchoolSeptFlag() {
		return schoolSeptFlag;
	}
	public void setSchoolSeptFlag(Integer schoolSeptFlag) {
		this.schoolSeptFlag = schoolSeptFlag;
	}
	public String getKindKey() {
		return kindKey;
	}
	public void setKindKey(String kindKey) {
		this.kindKey = kindKey;
	}
	

}
