package com.HuiShengTec.entity;

import java.io.Serializable;
import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

/**
 * @author llj
 *
 */
public class StudentInfo implements Serializable {
	private static final long serialVersionUID = 1L;
	
	private Integer id;
	private String stuCode;
	private Integer coachId;
	private String name;
	private String forShort;
	private String licenceCode;
	private Boolean sex;

	@DateTimeFormat(pattern="yyyy-MM-dd")
	private Date birthday;
	private Integer licenceId;
	
	@DateTimeFormat(pattern="yyyy-MM-dd")
	private Date createDate;
	private String phone;
	private String address;
	private Integer photoId;
	private Integer stuType;
	private Integer campusId;
	private Double cash;
	private Integer lessonStep;
	
	public Integer getLessonStep() {
		return lessonStep;
	}
	public void setLessonStep(Integer lessonStep) {
		this.lessonStep = lessonStep;
	}
	public Double getCash() {
		return cash;
	}
	public void setCash(Double cash) {
		this.cash = cash;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getStuCode() {
		return stuCode;
	}
	public void setStuCode(String stuCode) {
		this.stuCode = stuCode;
	}
	public Integer getCoachId() {
		return coachId;
	}
	public void setCoachId(Integer coachId) {
		this.coachId = coachId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getForShort() {
		return forShort;
	}
	public void setForShort(String forShort) {
		this.forShort = forShort;
	}
	public String getLicenceCode() {
		return licenceCode;
	}
	public void setLicenceCode(String licenceCode) {
		this.licenceCode = licenceCode;
	}
	public Boolean getSex() {
		return sex;
	}
	public void setSex(Boolean sex) {
		this.sex = sex;
	}
	public Date getBirthday() {
		return birthday;
	}
	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}
	public Integer getLicenceId() {
		return licenceId;
	}
	public void setLicenceId(Integer licenceId) {
		this.licenceId = licenceId;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public Integer getPhotoId() {
		return photoId;
	}
	public void setPhotoId(Integer photoId) {
		this.photoId = photoId;
	}
	public Integer getStuType() {
		return stuType;
	}
	public void setStuType(Integer stuType) {
		this.stuType = stuType;
	}
	public Integer getCampusId() {
		return campusId;
	}
	public void setCampusId(Integer campusId) {
		this.campusId = campusId;
	}
}
